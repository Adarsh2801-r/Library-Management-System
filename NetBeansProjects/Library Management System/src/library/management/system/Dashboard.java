    public void addActionToMenuLabels(){
        Component[] components = jPanel_menu.getComponents();
        for(Component component : components){
            if(component instanceof JLabel){
                JLabel label = (JLabel) component;
             
                label.addMouseListener(new MouseListener() {
                    @Override
                    public void mouseClicked(MouseEvent me) {
                        for(JLabel menuItem:menuLabels){
                            menuItem.setBackground(new Color(29,2,53));
                            menuItem.setForeground(Color.white);
                        }
                        label.setBackground(Color.white);
                        label.setForeground(Color.blue);
                       if(label==item4){
                            new Login().setVisible(true);
                            new Dashboard().setVisible(false);
                            dispose();


                       }
                       switch (label.getText().trim()){
                           case "DASHBOARD":
                                  showPanel(jPanel_dash);
                                  break;
                           case "LIBRARY RECORDS":
                                 showPanel(jPanel_record);
                                 break;
                                 
                            case "ABOUT US":
                                 showPanel(jPanel_about);
                                 break;
                                 
                                   
                       }

                    }

                    @Override
                    public void mousePressed(MouseEvent me) {
                        
                    }

                    @Override
                    public void mouseReleased(MouseEvent me) {
                    }

                    @Override
                    public void mouseEntered(MouseEvent me) {
                        label.setBorder(select_brdr);
                    }

                    @Override
                    public void mouseExited(MouseEvent me) {
                        label.setBorder(default_brdr);

                       
                    }
                });
                
            }
        }
    }

