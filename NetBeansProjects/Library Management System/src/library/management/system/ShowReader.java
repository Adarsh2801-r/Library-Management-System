    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        try{
        int id = Integer.parseInt(jTextField4.getText());
        conn con = new conn();
        String query = "select reg_id,firstname,lastname,email from readers where reg_id=?";
        PreparedStatement st = con.c.prepareStatement(query);
        st.setInt(1, id);
        ResultSet rs = st.executeQuery();
        while(rs.next()){
            jLabel15.setText(rs.getString("firstname"));
            jLabel14.setText(rs.getString("lastname"));
            jLabel13.setText(rs.getString("reg_id"));
            jLabel18.setText(rs.getString("email"));
        }
        st.close();
            rs.close();
            
            
            conn con1 = new conn();
        String query1 = "select phone from contacts where reg_id=?";
        PreparedStatement st1 = con1.c.prepareStatement(query1);
        st1.setInt(1, id);
        ResultSet rs1 = st1.executeQuery();
        while(rs1.next()){
            jLabel16.setText(rs1.getString("phone"));
            break;
            
        }
        while(rs1.next()){
            jLabel17.setText(rs1.getString("phone"));

        }
        st1.close();
            rs1.close();
        
        }
        catch(Exception e){
            e.printStackTrace();
        }
        
    }//GEN-LAST:event_jButton4ActionPerformed

